    public String getTextoSueldoBase() {
        String base = textoSueldoBase.getText().replace("$", "");
        base = base.replace(".", "");
        return base;
    }

    public void setTextoSueldoBase(String base) {
        try{
            Object value = Integer.parseInt(base);
            if (value instanceof Number) {
                textoSueldoBase.setHorizontalAlignment(JLabel.RIGHT);
                textoSueldoBase.setText(FORMAT.format(value));
            } else {
                textoSueldoBase.setHorizontalAlignment(JLabel.RIGHT);
                textoSueldoBase.setText("");
                textoSueldoBase.setText(FORMAT.format(value));
            }
        }catch(NumberFormatException e){
            textoSueldoBase.setHorizontalAlignment(JLabel.RIGHT);
            textoSueldoBase.setText("");
            textoSueldoBase.setText(FORMAT.format(sueldo_base));
        }
    }

