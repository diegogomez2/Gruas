    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed
       controladores.controladorUsuarios miControlador = new controladores.controladorUsuarios();
        String user;
        boolean selected = tablaUsuarios.getSelectedRowCount() > 0;
        if(selected){
            int row = getFilaSeleccionada();
            user = getUserFila(row);
            int dialogResult = JOptionPane.showOptionDialog(null, "Esta seguro que desea eliminar al usuario: \n " +
                    "User: " + user, "Confirmar", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, null, 0);
            if(dialogResult == JOptionPane.YES_OPTION)  miControlador.eliminarUsuario(user);
            JTabbedPane tabs = (JTabbedPane)this.getParent();
            miControlador.crearControladorPrincipal(tabs);
        }else{
            JOptionPane.showMessageDialog(null, "Debe seleccionar un cliente para ser eliminado");
        }
    }//GEN-LAST:event_botonEliminarActionPerformed


