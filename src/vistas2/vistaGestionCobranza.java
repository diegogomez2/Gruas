    public String getRadioGestion(){
        if(radioVolverALlamar.isSelected()){
            return "Volver a llamar";
        }else if(radioRetirarPago.isSelected()){
            return "Retirar pago";
        }else{
            return "Transferencia";
        }
    }
    
    public String getFecha(){
        Date fecha = textoFecha.getDate();
        if(fecha == null) return "";
        String dateString = formatDate.format(textoFecha.getDate());
        return dateString;
    }
    
